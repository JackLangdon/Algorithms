import java.util.Scanner;

public class shellSort {

	private int[] array;
	private int size;
	private int min;
	private int max;
	private int swapCount;

	public void buildArray(int size, int min, int max) {
		this.array = new int[size];
		for (int i = 0; i < this.array.length; i++) {
			this.array[i] = (int) (Math.random() * (max - min + 1) + min);
		}
	}

	public void printArray() {
		for (int i = 0; i < this.array.length; i++) {
			System.out.print("------");
		}
		System.out.println();
		for (int i = 0; i < this.array.length; i++) {
			if (i < 10) {
				System.out.print("| 0" + i + " |");
			} else {
				System.out.print("| " + i + " |");
			}
		}
		System.out.println();
		for (int i = 0; i < this.array.length; i++) {
			System.out.print("------");
		}
		System.out.println();
		for (int i = 0; i < this.array.length; i++) {
			System.out.print("| " + this.array[i] + " |");
		}
		System.out.println();
		for (int i = 0; i < this.array.length; i++) {
			System.out.print("------");
		}
		System.out.println();
	}

	public void sort() {
		int inner, outer, temp;
		int interval = 1;

		// Set an appropriate interval
		while (interval <= this.array.length / 3) {
			interval = interval * 3 + 1;
		}
		System.out.println("Interval set at " + interval + "\n");

		while (interval > 0) {

			for (outer = interval; outer < this.array.length; outer++) {

				temp = this.array[outer];

				inner = outer;
				System.out.println("Comparing index " + (inner - interval) + " with index " + outer);

				System.out.println("Checking if " + this.array[inner - interval] + " is bigger than " + temp);

				while (inner > (interval - 1) && this.array[inner - interval] > temp) {
					System.out.println("Inside loop - checking intervals of " + interval + ".");
					System.out.println("\tChecking if index " + (inner - interval) + " is bigger than '" + temp + "'");

					this.array[inner] = this.array[inner - interval];

					System.out.println("\t'" + this.array[inner - interval] + "' moved to index " + inner);

					inner -= interval;

					// Increment swap count
					this.swapCount++;

				}

				// Move smaller value to lower index
				this.array[inner] = temp;
				// Increment swap count
				this.swapCount++;

				System.out.println("\t'" + temp + "' moved to index " + inner);

				printArray();
			}

			// Reduce interval
			interval = (interval - 1) / 3;
			System.out.println("New Interval: " + interval + "\n");
		}
	}

	public shellSort() {
		this.swapCount = 0;

		Scanner sc = new Scanner(System.in);

		System.out.println("Enter array size:");
		this.size = sc.nextInt();

		System.out.println("Enter min value:");
		this.min = sc.nextInt();

		System.out.println("Enter max value:");
		this.max = sc.nextInt();

		sc.close();

		buildArray(this.size, this.min, this.max);
		System.out.println("Initial array:");
		System.out.println();
		printArray();

		sort();

		System.out.println();
		System.out.println("Final array:");
		System.out.println();
		printArray();

		System.out.println();
		System.out.println("Total num of swaps required: " + this.swapCount);
	}

	public static void main(String[] args) {
		shellSort shellSort = new shellSort();
	}
}


